    # –≤ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–µ –ü–∞—Å–∫–∞–ª—è –ø—Ä–∏ –Ω—É–ª–µ–≤–æ–º –∑–Ω–∞—á–µ–Ω–∏–∏
    if n == 0:
        list1 = [1]
        print(list1)
    else:
        # –ø–µ—Ä–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç —Å—Ç—Ä–æ–∫–∏ –≤—Å–µ–≥–¥–∞ —Ä–∞–≤–µ–Ω 1
        list1=[1]
