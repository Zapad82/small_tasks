# —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è —Ä–∞—Å—á–µ—Ç–∞ –∑–Ω–∞—á–µ–Ω–∏–π —Ä—è–¥–∞
def pascal():
    # –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –≤–≤–æ–¥ –Ω–æ–º–µ—Ä–∞ —Å—Ç—Ä–æ–∫–∏ –≤ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–µ –ü–∞—Å–∫–∞–ª—è
    n = int(input())
    # –≤ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–µ –ü–∞—Å–∫–∞–ª—è –ø—Ä–∏ –Ω—É–ª–µ–≤–æ–º –∑–Ω–∞—á–µ–Ω–∏–∏
    if n == 0:
        list1 = [1]
        print(*list1)
    for i in range(n):
        if i == 0:
            list1 = [1]
            print(*list1, sep=" ")
        else:
            # –ø–µ—Ä–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç —Å—Ç—Ä–æ–∫–∏ –≤—Å–µ–≥–¥–∞ —Ä–∞–≤–µ–Ω 1
            list1=[1]
            # —Å–ø–∏—Å–æ–∫ –¥–ª—è –∑–µ—Ä–∫–∞–ª—å–Ω–æ–≥–æ –æ—Ç—Ä–∞–∂–µ–Ω–∏—è —Å–ø–∏—Å–∫–∞ list1
            list2=[]
            # –∏–∑–º–µ–Ω—è–µ–º–∞—è –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è –ø–æ–∏—Å–∫–∞ —ç–ª–µ–º–µ–Ω—Ç–∞ –Ω–∞ —Å–ª–µ–¥—É—é—â–µ–º —à–∞–≥–µ
            k = 1
            # –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è –ø–æ–∏—Å–∫–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Å—Ç—Ä–æ–∫–∏
            elem = 1
            nn = i
            # —Ü–∏–∫–ª –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ–∫–∞ –≤–≤–µ–¥–µ–Ω–Ω–æ–µ —á–∏—Å–ª–æ –∏–∑–º–µ–Ω—è–µ—Ç—Å—è –∏ –±–æ–ª—å—à–µ k
            while nn > k:
                # —Ñ–æ—Ä–º—É–ª–∞ —Ä–∞—Å—á–µ—Ç–∞ —Å–ª–µ–¥—É—é—â–µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞
                elem *= (nn) / k
                # –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ —Å–ø–∏—Å–æ–∫ list1
                list1.append(round(elem))
                # —É–º–µ–Ω—å—à–∞–µ–º –≤–≤–µ–¥–µ–Ω–Ω–æ–µ —á–∏—Å–ª–æ –Ω–∞ –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏ –Ω–∞ 1
                k += 1
                # —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –Ω–∞ –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏ –Ω–∞ 1 —Å–æ–≥–ª–∞—Å–Ω–æ —Ñ–æ—Ä–º—É–ª–µ
                nn -= 1

            # –µ—Å–ª–∏ –≤–≤–µ–Ω–Ω–æ–µ —á–∏—Å–ª–æ —á–µ—Ç–Ω–æ–µ
            if i % 2 == 0:
                # —Å –ø–æ–º–æ—â—å—é —Å—Ä–µ–∑–æ–≤ —Å–æ–∑–¥–∞–µ–º –æ—Ç–∑–µ—Ä–∫–∞–ª–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å —É–¥–∞–ª–µ–Ω–∏–µ–º –ª–∏—à–Ω–µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞
                list2 = list1[::-1][1:]
                list1.extend(list2)
                print(*list1, sep=" ")
            # –µ—Å–ª–∏ –Ω–µ—á–µ—Ç–Ω–æ–µ
            else:
                list2 = list1[::-1]
                list1.extend(list2)
                print(*list1, sep=" ")

# –≤—ã–∑–æ–≤ —Ñ—É–Ω–∫—Ü–∏–∏
pascal()